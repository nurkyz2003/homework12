public class Cat {
    private  String name;
    private  int age;
    private String color;
    private String eyecolor;
    private double weight;

    void setName(String word){
        name = word;
    }

    void setAge(int number){
        age = number;
    }

    void setColor(String word1){
        color = word1;
    }

    void setEyecolor(String word2){
        eyecolor = word2;
    }

    void setWeight(double number1){
        weight = number1;
    }

    String getName(){
        return name;
    }

    int getAge(){
        return age;
    }
    String getColor(){
        return color;
    }

    String getEyecolor(){
        return eyecolor;
    }

    double getWeight(){
        return weight;
    }

 }

